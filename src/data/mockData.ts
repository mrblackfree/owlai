// Helper function to get logo URL
export const getToolLogoUrl = (tool: Tool | undefined): string => {
  if (!tool) return `https://ui-avatars.com/api/?name=Tool&background=6366f1&color=fff&bold=true&format=svg`;
  if (tool.imageUrl) return tool.imageUrl;
  
  // Fallback to UI Avatars if website is not available
  if (!tool.website) {
    return `https://ui-avatars.com/api/?name=${encodeURIComponent(tool.name)}&background=6366f1&color=fff&bold=true&format=svg`;
  }
  
  // Try Clearbit first
  const domain = tool.website.replace(/^https?:\/\//, '');
  const clearbitUrl = `https://logo.clearbit.com/${domain}`;
  
  // Fallback to UI Avatars
  const fallbackUrl = `https://ui-avatars.com/api/?name=${encodeURIComponent(tool.name)}&background=6366f1&color=fff&bold=true&format=svg`;
  
  return clearbitUrl || fallbackUrl;
};

